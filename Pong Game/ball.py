from turtle import Turtle
# Create "ball" (class)
# Ball bouncing functionality -- If it touches a wall or a paddle, turn n degrees (method in class)


class Ball(Turtle):

    def __init__(self):
        super().__init__()
        self.shape("circle")
        self.color("white")
        self.pu()
        self.x_move = 10
        self.y_move = 10

    def move(self):
        new_x = self.xcor() + self.x_move
        new_y = self.ycor() + self.y_move
        self.setpos(new_x, new_y)

    def wall_bounce(self):
        self.y_move *= -1

    def paddle_bounce(self):
        self.x_move *= -1

    def restart(self):
        self.goto(0, 0)
        self.paddle_bounce()
